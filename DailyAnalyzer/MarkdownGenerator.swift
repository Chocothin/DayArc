    // MARK: - Helper Functions

    /// Generate rating bar visualization
    static func ratingBar(_ score: Double) -> String {
        let filled = Int(score / 10)
        let empty = 10 - filled
        return String(repeating: "█", count: filled) + String(repeating: "░", count: empty)
    }

    /// Find best day from daily stats
    private static func bestDay(from stats: [DailyStats]) -> String {
        guard let best = stats.max(by: { $0.productivityScore.totalScore < $1.productivityScore.totalScore }) else {
            return "N/A"
        }
        let formatter = DateFormatter()
        formatter.dateFormat = "MMM d"
        return "\(formatter.string(from: best.date)) (\(Int(best.productivityScore.totalScore)))"
    }

    /// Find most consistent day
    private static func mostConsistent(from stats: [DailyStats]) -> String {
        guard let best = stats.max(by: { $0.productivityScore.consistencyScore < $1.productivityScore.consistencyScore }) else {
            return "N/A"
        }
        let formatter = DateFormatter()
        formatter.dateFormat = "MMM d"
        return formatter.string(from: best.date)
    }

    /// Calculate average score
    private static func averageScore(from stats: [DailyStats]) -> Double {
        guard !stats.isEmpty else { return 0 }
        let total = stats.reduce(0.0) { $0 + $1.productivityScore.totalScore }
        return total / Double(stats.count)
    }

    /// Calculate average hours
    private static func averageHours(from stats: [DailyStats]) -> Double {
        guard !stats.isEmpty else { return 0 }
        let totalSeconds = stats.reduce(0.0) { $0 + $1.totalActiveTime }
        return (totalSeconds / Double(stats.count)) / 3600.0
    }

    /// Find most productive week index
    private static func mostProductiveWeek(from stats: [DailyStats]) -> Int {
        guard !stats.isEmpty,
              let maxIndex = stats.enumerated().max(by: { $0.element.productivityScore.totalScore < $1.element.productivityScore.totalScore })?.offset else {
            return 1
        }
        return maxIndex + 1
    }

    /// Delta string between current and baseline
    static func deltaString(current: Double, baseline: Double) -> String {
        let delta = current - baseline
        let arrow = delta > 0 ? "▲" : (delta < 0 ? "▼" : "・")
        return "\(arrow) \(String(format: "%.1f", delta))"
    }
