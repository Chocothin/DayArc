            let totalTimeDelta = (monthlyStats.totalActiveTime - prev.totalActiveTime) / 3600.0
            let productivityDelta = monthlyStats.productivityScore.totalScore - prev.productivityScore.totalScore
            let deepWorkDelta = deepWorkAnalysis.totalDeepWorkTime -
                StatsCalculator.calculateDeepWorkAnalysis(from: activities, thresholdMinutes: 25).totalDeepWorkTime

            md += "\n## 📊 월간 성장 지표\n\n"
            md += "| 지표 | \(prevStr) | \(display) | 변화량 |\n"
            md += "|------|------------|----------|--------|\n"
            md += "| 총 업무 시간 | \(String(format: "%.1f", prev.totalActiveTime / 3600))h | \(String(format: "%.1f", monthlyStats.totalActiveTime / 3600))h | \(String(format: "%+.1f", totalTimeDelta))h |\n"
            md += "| 생산성 점수 | \(Int(prev.productivityScore.totalScore)) | \(Int(monthlyStats.productivityScore.totalScore)) | \(String(format: "%+.0f", productivityDelta)) |\n"
            md += "| Deep Work | - | \(String(format: "%.1f", deepWorkAnalysis.totalDeepWorkTime))h | - |\n"
            md += "| 주요 활동 | \(prev.topApps.first?.appName ?? "-") | \(monthlyStats.topApps.first?.appName ?? "-") | - |\n\n"
        }

        if blocks.includeCharts && !chartPaths.isEmpty {
            md += "\n## 📈 월간 시각화\n\n"

            // Add chart references based on what was generated
            for chartPath in chartPaths {
                let filename = chartPath.lastPathComponent

                // Add appropriate section heading based on chart type
                if filename.contains("heatmap") {
                    md += "\n### Monthly Productivity Heatmap\n"
                } else if filename.contains("app_breakdown") {
                    md += "\n### App Usage Breakdown\n"
                } else if filename.contains("weekly_trend") {
                    md += "\n### Weekly Productivity Trend\n"
                }

                md += "![[charts/\(filename)]]\n"
            }
        }

        if blocks.includeFooter {
            md += "\n---\n*Generated by DayArc on \(Date().formatted())*\n"
        }

        return md
    }
