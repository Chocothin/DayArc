    // MARK: Helpers
    private static func ratingBar(_ score: Double) -> String {
        let filled = Int((score / 100) * 10)
        let empty = 10 - filled
        return String(repeating: "█", count: filled) + String(repeating: "░", count: empty)
    }

    private static func scoreGrade(_ score: Double) -> String {
        switch score {
        case 90...100: return "S"
        case 80..<90: return "A"
        case 70..<80: return "B"
        case 60..<70: return "C"
        case 50..<60: return "D"
        default: return "F"
        }
    }

    static func deltaString(_ current: Double, _ baseline: Double) -> String {
        let d = current - baseline
        if d > 0 { return "▲ " + String(format: "%.1f", d) }
        if d < 0 { return "▼ " + String(format: "%.1f", abs(d)) }
        return "—"
    }

    // MARK: Daily
    static func daily(date: Date, stats: DailyStats, analysis: AnalysisResult?, activities: [TimelineCard], history: MarkdownGenerator.HistoricalContext?, chartPaths: [URL], language: ReportLanguage, blocks: ReportBlocks, timelineSummaries: [TimelineSummary] = []) -> String {
        let s = LangStrings(language: language)
        let fmt = DateFormatter()
        fmt.dateFormat = "yyyy-MM-dd"
        let dateString = fmt.string(from: date)
        // Note: fullDate can be used if needed for expanded date display
        // fmt.dateFormat = language == .korean ? "yyyy년 M월 d일 EEEE" : "EEEE, MMMM d, yyyy"
        // let fullDate = fmt.string(from: date)
        let timeFmt = DateFormatter()
        timeFmt.dateFormat = language == .korean ? "a h:mm" : "h:mm a"

        // Conditional calculation: only compute analyses when needed
        let needsDeepWork = blocks.includeStats || blocks.includeDeepWorkAnalysis || blocks.includeRecommendations || blocks.includeScorecard
        let needsDistraction = blocks.includeStats || blocks.includeDistractionBreakdown || blocks.includeRecommendations || blocks.includeScorecard
        let needsContext = blocks.includeStats || blocks.includeContextSwitching || blocks.includeRecommendations || blocks.includeScorecard
        let deepWorkAnalysis: DeepWorkAnalysis? = needsDeepWork ? StatsCalculator.calculateDeepWorkAnalysis(from: activities, thresholdMinutes: 25) : nil
        let distractionAnalysis: DistractionAnalysis? = needsDistraction ? StatsCalculator.calculateDistractionAnalysis(from: activities) : nil
        let contextAnalysis: ContextSwitchingAnalysis? = needsContext ? StatsCalculator.calculateContextSwitchingAnalysis(from: activities) : nil
        // Note: focusBlocks can be generated on-demand if needed in the future
        // let focusBlocks = blocks.includeFocusBlocks ? StatsCalculator.generateFocusBlocks(from: activities, minimumMinutes: 30) : []

        // Calculate key highlights
        let longestSession = activities.max(by: { $0.duration < $1.duration })
        let deepWorkHours = deepWorkAnalysis?.totalDeepWorkTime ?? 0
        let deepWorkPercent = stats.totalActiveTime > 0 ? (deepWorkHours * 3600 / stats.totalActiveTime * 100) : 0
        let deepWorkTarget = 40.0
        let deepWorkGap = deepWorkPercent - deepWorkTarget

        // Productivity status
        let productivityStatus = stats.productivityScore.totalScore >= 70 ? "우수 ✨" : stats.productivityScore.totalScore >= 50 ? "양호 ✅" : "개선 필요 ⚠️"

        var md = "# ✅ Daily Note - \(dateString)\n\n"

        if blocks.includeStartRoutine {
            md += "## \(s.t(.startRoutine))\n\n"
            md += "- [ ] 기상 시간:\n"
            md += "- [ ] 수면 시간:\n"
            md += "- [ ] 컨디션 (1~10):\n\n"
        }

        if blocks.includeTodo {
            md += "## \(s.t(.todo))\n\n"
            md += "- [ ]\n"
            md += "- [ ]\n"
            md += "- [ ]\n\n"
        }

        if blocks.includeFocusBlocks {
            md += "## \(s.t(.focusBlocks))\n\n"
            md += "|시간|활동|집중도|방해요소|개선점|\n"
            md += "|---|---|---|---|---|\n"
            md += "|||||\n\n"
        }

        if blocks.includeEmotion {
            md += "## \(s.t(.emotion))\n\n"
            md += "- 감정 점수:\n"
            md += "- 잘한 것 & 좋았던 것:\n"
            md += "- 못한 것 & 안좋았던 것:\n\n"
        }

        if blocks.includeInsights {
            md += "## \(s.t(.insights))\n\n"
            md += ">\n\n"
        }

        if blocks.includeRecap {
            md += "## \(s.t(.recap))\n\n"
            md += ">\n\n"
        }

        md += "\n\n---\n\n"

        // Dayflow Auto Analysis Header - show if any analysis section is enabled
        let hasAnalysisSections = blocks.includeStats || blocks.includeTimeline || blocks.includeCategoriesAndApps ||
                                  blocks.includeCharts || blocks.includeDeepWorkAnalysis || blocks.includeDistractionBreakdown ||
                                  blocks.includeContextSwitching || blocks.includeRecommendations || blocks.includeScorecard
        if hasAnalysisSections {
             md += "# 📊 \(s.t(.dayflow))\n\n"
        }

        if blocks.includeStats {
            // Safely unwrap optional analyses (they will exist because needsDeepWork/needsDistraction/needsContext are true when includeStats is true)
            let distraction = distractionAnalysis ?? DistractionAnalysis(distractingAppTime: 0, interruptionCount: 0, shortSessionCount: 0, hourlyDistractionCount: [:], peakDistractionHours: nil)
            let context = contextAnalysis ?? ContextSwitchingAnalysis(totalSwitches: 0, averageSessionLength: 0, flowSessions: [], frequentSwitchPatterns: [])

            md += "## ⭐ \(s.t(.highlights))\n\n"
            md += "1. 🔥 **\(longestSession?.appName ?? "활동 없음")** (\(Int((longestSession?.duration ?? 0) / 60))분) - 최장 집중 작업\n"
            md += "2. 🟠 **생산성 \(String(format: "%.1f", stats.productivityScore.totalScore))점** - \(productivityStatus)\n"
            md += "3. ⚠️ **Deep Work \(String(format: "%.1f", deepWorkHours))시간 (\(Int(deepWorkPercent))%)** - 목표까지 \(abs(Int(deepWorkGap)))% \(deepWorkGap >= 0 ? "초과" : "부족")\n\n"
            md += "---\n\n"

            md += "## 📈 \(s.t(.stats))\n\n"
            md += "- **총 업무 시간**: \(String(format: "%.1f", stats.totalActiveTime / 3600))시간 (\(Int(stats.totalActiveTime / 60))분)\n"
            md += "- **집중 작업 시간 (Deep Work)**: \(String(format: "%.1f", deepWorkHours))시간 (\(Int(deepWorkHours * 60))분)\n"
            md += "- **생산성 점수**: \(String(format: "%.1f", stats.productivityScore.totalScore))/100\n"
            md += "- **주요 활동**: \(activities.first?.appName ?? "없음")\n"
            md += "- **방해 앱 세션**: \(distraction.interruptionCount)회\n\n"

            md += "### 🔍 생산성 점수 분석\n\n"
            md += "**총점: \(String(format: "%.1f", stats.productivityScore.totalScore))/100**\n\n"

            md += "- **집중 작업 (Deep Work)**: \(String(format: "%.1f", stats.productivityScore.deepWorkScore))/100 (가중치 45%)\n"
            md += "  - 📊 Deep Work 비율: \(Int(deepWorkPercent))% - \(String(format: "%.1f", deepWorkHours))시간 / \(String(format: "%.1f", stats.totalActiveTime / 3600))시간\n\n"

            md += "- **집중도 (방해 없음)**: \(String(format: "%.1f", max(0, 100 - stats.productivityScore.distractionScore)))/100 (가중치 30%)\n"
            md += "  - 📊 방해 앱 세션: \(distraction.interruptionCount)회, 짧은 세션: \(distraction.shortSessionCount)회\n\n"

            md += "- **업무 균형**: \(String(format: "%.1f", stats.productivityScore.diversityScore))/100 (가중치 15%)\n"
            md += "  - 📊 앱 다양성: \(stats.uniqueApps)개 앱 사용 (2-3개 최적)\n\n"

            md += "- **업무 일관성**: \(String(format: "%.1f", stats.productivityScore.consistencyScore))/100 (가중치 10%)\n"
            md += "  - 📊 평균 세션 길이: \(String(format: "%.0f", context.averageSessionLength))분/블록\n\n"

            md += "**💡 개선 제안:**\n\n"
            md += "- 💡 **Deep Work 시간 늘리기**: 25분 이상 집중 작업 세션을 더 많이 만들어보세요. 현재 \(Int(deepWorkPercent))%, 목표 40%+\n\n"
            md += "- 💡 **작업 블록 늘리기**: 평균 작업 시간이 \(String(format: "%.0f", context.averageSessionLength))분입니다. 30-60분 단위로 작업을 계획해보세요.\n\n"
        }

        // Deep Work Analysis Section
        if blocks.includeDeepWorkAnalysis, let deepWork = deepWorkAnalysis {
            // Calculate derived metrics from DeepWorkAnalysis
            let sessionCount = deepWork.sessions.count
            let avgSessionLength = sessionCount > 0 ? (deepWork.totalDeepWorkTime * 60) / Double(sessionCount) : 0
            let longestSessionDW = deepWork.sessions.map { $0.duration / 60 }.max() ?? 0  // duration is in seconds

            md += "\n## 🎯 Deep Work 분석\n\n"
            md += "| 지표 | 값 | 설명 |\n"
            md += "|------|-----|------|\n"
            md += "| 총 Deep Work 시간 | \(String(format: "%.1f", deepWork.totalDeepWorkTime))시간 | 25분 이상 집중 작업 |\n"
            md += "| Deep Work 비율 | \(Int(deepWorkPercent))% | 전체 업무 시간 대비 |\n"
            md += "| Deep Work 세션 수 | \(sessionCount)회 | 25분+ 집중 블록 |\n"
            md += "| 평균 Deep Work 길이 | \(String(format: "%.0f", avgSessionLength))분 | 세션당 평균 |\n"
            md += "| 최장 Deep Work | \(String(format: "%.0f", longestSessionDW))분 | 가장 긴 집중 시간 |\n\n"

            if deepWorkPercent < 40 {
                md += "💡 **개선 제안**: Deep Work 비율이 \(Int(deepWorkPercent))%입니다. 목표 40% 달성을 위해 방해 요소를 줄이고 집중 블록을 늘려보세요.\n\n"
            } else {
                md += "✅ **좋은 성과**: Deep Work 비율 \(Int(deepWorkPercent))%로 목표(40%)를 달성했습니다!\n\n"
            }
        }

        // Distraction Breakdown Section
        if blocks.includeDistractionBreakdown, let distraction = distractionAnalysis {
            // Calculate derived metrics from DistractionAnalysis
            let distractionTimeRatio = stats.totalActiveTime > 0 ? (distraction.distractingAppTime * 3600 / stats.totalActiveTime) : 0
            let totalDistractionMinutes = distraction.distractingAppTime * 60

            md += "\n## ⚡ 방해 요소 분석\n\n"
            md += "| 지표 | 값 | 영향 |\n"
            md += "|------|-----|------|\n"
            md += "| 방해 앱 세션 | \(distraction.interruptionCount)회 | 집중 흐름 중단 |\n"
            md += "| 짧은 세션 (5분 미만) | \(distraction.shortSessionCount)회 | 얕은 작업 |\n"
            md += "| 방해 앱 시간 | \(String(format: "%.0f", totalDistractionMinutes))분 | 비생산적 시간 |\n"
            md += "| 방해 시간 비율 | \(String(format: "%.1f", distractionTimeRatio * 100))% | 전체 대비 |\n\n"

            // Peak distraction hours
            if let peakHours = distraction.peakDistractionHours {
                md += "### ⏰ 방해가 잦은 시간대\n\n"
                md += "**\(peakHours.start):00 ~ \(peakHours.end):00** 사이에 방해가 가장 많았습니다 (\(peakHours.count)회)\n\n"
            }

            if distraction.interruptionCount > 10 {
                md += "⚠️ **주의**: 방해 세션이 \(distraction.interruptionCount)회로 많습니다. 알림 설정을 검토하고 집중 모드 사용을 고려해보세요.\n\n"
            }
        }

        // Context Switching Analysis Section
        if blocks.includeContextSwitching, let context = contextAnalysis {
            // Calculate derived metrics from ContextSwitchingAnalysis
            let activeHours = stats.totalActiveTime / 3600
            let switchesPerHour = activeHours > 0 ? Double(context.totalSwitches) / activeHours : 0
            let longestContinuousWork = context.flowSessions.map { $0.duration * 60 }.max() ?? 0  // duration is in hours, convert to minutes

            md += "\n## 🔄 컨텍스트 전환 분석\n\n"
            md += "| 지표 | 값 | 의미 |\n"
            md += "|------|-----|------|\n"
            md += "| 총 전환 횟수 | \(context.totalSwitches)회 | 앱/작업 간 전환 |\n"
            md += "| 시간당 전환 | \(String(format: "%.1f", switchesPerHour))회 | 집중도 지표 |\n"
            md += "| 평균 세션 길이 | \(String(format: "%.0f", context.averageSessionLength))분 | 작업당 평균 시간 |\n"
            md += "| 가장 긴 연속 작업 | \(String(format: "%.0f", longestContinuousWork))분 | 최대 집중 시간 |\n\n"

            let switchLevel: String
            if switchesPerHour < 4 {
                switchLevel = "✅ **우수**: 시간당 전환이 적어 집중도가 높습니다."
            } else if switchesPerHour < 8 {
                switchLevel = "⚠️ **보통**: 전환 빈도가 약간 높습니다. 작업 블록을 더 길게 유지해보세요."
            } else {
                switchLevel = "🔴 **주의**: 전환이 매우 잦습니다. 멀티태스킹을 줄이고 단일 작업에 집중해보세요."
            }
            md += "\(switchLevel)\n\n"
        }

        // Recommendations Section
        if blocks.includeRecommendations {
            // Safely unwrap optional analyses
            let distraction = distractionAnalysis ?? DistractionAnalysis(distractingAppTime: 0, interruptionCount: 0, shortSessionCount: 0, hourlyDistractionCount: [:], peakDistractionHours: nil)
            let context = contextAnalysis ?? ContextSwitchingAnalysis(totalSwitches: 0, averageSessionLength: 0, flowSessions: [], frequentSwitchPatterns: [])

            md += "\n## 💪 실행 가능한 추천\n\n"

            // Calculate metrics for recommendations
            let activeHoursForRecs = stats.totalActiveTime / 3600
            let switchesPerHourForRecs = activeHoursForRecs > 0 ? Double(context.totalSwitches) / activeHoursForRecs : 0

            var recommendations: [String] = []

            // Deep Work recommendations
            if deepWorkPercent < 40 {
                recommendations.append("🎯 **Deep Work 늘리기**: 25분 이상 집중 작업 세션을 더 만들어보세요. 현재 \(Int(deepWorkPercent))%, 목표 40%+")
            }

            // Distraction recommendations
            if distraction.interruptionCount > 10 {
                recommendations.append("🔕 **알림 관리**: 방해 앱 알림을 끄고 집중 모드를 활성화해보세요. 현재 \(distraction.interruptionCount)회 중단")
            }

            // Context switching recommendations
            if switchesPerHourForRecs > 8 {
                recommendations.append("⏱️ **작업 블록화**: 30-60분 단위로 작업을 계획하고 타이머를 사용해보세요. 현재 시간당 \(String(format: "%.1f", switchesPerHourForRecs))회 전환")
            }

            // Session length recommendations
            if context.averageSessionLength < 15 {
                recommendations.append("📊 **세션 연장**: 평균 세션이 \(String(format: "%.0f", context.averageSessionLength))분으로 짧습니다. 작업 시작 전 명확한 목표를 설정해보세요.")
            }

            // Productivity score recommendations
            if stats.productivityScore.totalScore < 50 {
                recommendations.append("📈 **생산성 개선**: 생산성 점수가 \(String(format: "%.0f", stats.productivityScore.totalScore))점입니다. 가장 중요한 작업을 오전에 배치해보세요.")
            }

            if recommendations.isEmpty {
                md += "🎉 **훌륭합니다!** 오늘의 작업 패턴이 전반적으로 좋습니다. 이 리듬을 유지하세요!\n\n"
            } else {
                for (idx, rec) in recommendations.enumerated() {
                    md += "\(idx + 1). \(rec)\n\n"
                }
            }
        }

        // Scorecard Section
        if blocks.includeScorecard {
            // Safely unwrap optional analyses
            let distraction = distractionAnalysis ?? DistractionAnalysis(distractingAppTime: 0, interruptionCount: 0, shortSessionCount: 0, hourlyDistractionCount: [:], peakDistractionHours: nil)
            let context = contextAnalysis ?? ContextSwitchingAnalysis(totalSwitches: 0, averageSessionLength: 0, flowSessions: [], frequentSwitchPatterns: [])

            md += "\n## 📋 생산성 스코어카드\n\n"

            // Calculate individual scores for scorecard
            let deepWorkScore = min(100, deepWorkPercent * 2.5)  // 40% = 100점
            let focusScore = max(0, 100 - Double(distraction.interruptionCount) * 5)
            let consistencyScore = min(100, context.averageSessionLength * 3.33)  // 30분 = 100점
            let balanceScore = stats.productivityScore.diversityScore

            md += "| 항목 | 점수 | 바 | 등급 |\n"
            md += "|------|------|-----|------|\n"
            md += "| 🎯 Deep Work | \(Int(deepWorkScore))/100 | \(ratingBar(deepWorkScore)) | \(scoreGrade(deepWorkScore)) |\n"
            md += "| 🔕 집중도 | \(Int(focusScore))/100 | \(ratingBar(focusScore)) | \(scoreGrade(focusScore)) |\n"
            md += "| ⏱️ 일관성 | \(Int(consistencyScore))/100 | \(ratingBar(consistencyScore)) | \(scoreGrade(consistencyScore)) |\n"
            md += "| ⚖️ 균형 | \(Int(balanceScore))/100 | \(ratingBar(balanceScore)) | \(scoreGrade(balanceScore)) |\n"
            md += "| **📊 종합** | **\(Int(stats.productivityScore.totalScore))/100** | **\(ratingBar(stats.productivityScore.totalScore))** | **\(scoreGrade(stats.productivityScore.totalScore))** |\n\n"

            // Overall assessment
            let overallAssessment: String
            if stats.productivityScore.totalScore >= 80 {
                overallAssessment = "🏆 **탁월한 하루**였습니다! 이 패턴을 유지하세요."
            } else if stats.productivityScore.totalScore >= 60 {
                overallAssessment = "✅ **좋은 하루**였습니다. 약간의 개선으로 더 나아질 수 있습니다."
            } else if stats.productivityScore.totalScore >= 40 {
                overallAssessment = "⚠️ **보통의 하루**였습니다. 집중 시간을 늘려보세요."
            } else {
                overallAssessment = "🔴 **개선이 필요한 하루**입니다. 방해 요소를 줄이고 계획을 세워보세요."
            }
            md += overallAssessment + "\n\n"
        }

        // Only append timeline if requested
        if blocks.includeTimeline {
             md += "\n## 🎯 \(s.t(.timeline))\n"
            // Group activities by similar time periods and preserve full TimelineCard data
            struct ActivityGroup {
                var startTime: Date
                var endTime: Date
                var activities: [TimelineCard]
                var duration: Double
            }

            var activityGroups: [ActivityGroup] = []

            // Group consecutive activities within 5 minutes
            for activity in activities.sorted(by: { $0.startAt < $1.startAt }) {
                if let lastGroupIndex = activityGroups.indices.last,
                   activity.startAt.timeIntervalSince(activityGroups[lastGroupIndex].endTime) < 300 {
                    // Extend the last group
                    activityGroups[lastGroupIndex].endTime = activity.startAt.addingTimeInterval(activity.duration)
                    activityGroups[lastGroupIndex].activities.append(activity)
                    activityGroups[lastGroupIndex].duration += activity.duration
                } else {
                    // Start a new group
                    activityGroups.append(ActivityGroup(
                        startTime: activity.startAt,
                        endTime: activity.startAt.addingTimeInterval(activity.duration),
                        activities: [activity],
                        duration: activity.duration
                    ))
                }
            }

            // Generate timeline entries with detailed activity information from Dayflow SQLite
            for (index, group) in activityGroups.prefix(10).enumerated() {
                let startTimeStr = timeFmt.string(from: group.startTime)
                let endTimeStr = timeFmt.string(from: group.endTime)
                let durationMin = Int(group.duration / 60)

                // Extract unique apps
                let apps = Array(Set(group.activities.map { $0.appName }))

                // Use AI-generated summary if available
                if index < timelineSummaries.count {
                    let summary = timelineSummaries[index]

                    // Add category if available
                    let categoryInfo = summary.category.map { " | \($0)" } ?? ""

                    md += "\n### \(startTimeStr) - \(endTimeStr)\(categoryInfo)\n\n"
                    md += "**\(summary.title)**\n\n"
                    md += "\(summary.summary)\n\n"
                    md += "---\n\n"
                } else {
                    // Fallback: use raw data
                    md += "\n### \(startTimeStr) - \(endTimeStr) (\(durationMin)분)\n\n"
                    md += "**사용 도구:** \(apps.joined(separator: ", "))\n\n"
                    md += "**활동 내용:**\n\n"

                    // Collect all summaries and detailed info from the group
                    var contentParts: [String] = []

                    for activity in group.activities {
                        let activityDurationMin = Int(activity.duration / 60)
                        let activityTime = timeFmt.string(from: activity.startAt)

                        // Use detailed_summary if available, fallback to summary, then windowTitle
                        if let detailedSummary = activity.detailedSummary, !detailedSummary.isEmpty {
                            contentParts.append("[\(activityTime), \(activityDurationMin)분] \(activity.appName): \(detailedSummary)")
                        } else if let summary = activity.summary, !summary.isEmpty {
                            contentParts.append("[\(activityTime), \(activityDurationMin)분] \(activity.appName): \(summary)")
                        } else if let windowTitle = activity.windowTitle, !windowTitle.isEmpty {
                            var part = "[\(activityTime), \(activityDurationMin)분] \(activity.appName): \(windowTitle)"
                            if let url = activity.url, !url.isEmpty {
                                part += " (\(url))"
                            }
                            contentParts.append(part)
                        } else {
                            contentParts.append("[\(activityTime), \(activityDurationMin)분] \(activity.appName)")
                        }
                    }

                    // Combine all content
                    let fullContent = contentParts.joined(separator: "\n\n")

                    md += "\(fullContent)\n\n"
                    md += "---\n\n"
                }
            }
        }

        // AI Insights Section
        if blocks.includeAISection {
             // Only show AI section if requested
             if let analysis = analysis {
                 Logger.shared.debug("🎨 Markdown: Adding AI insights section", source: "MarkdownGen")
                 Logger.shared.debug("🎨 Markdown: Summary='\(analysis.summary.prefix(50))', Insights=\(analysis.insights.count), Recs=\(analysis.recommendations.count)", source: "MarkdownGen")

                 let insightsText = analysis.insights.map { "- " + $0 }.joined(separator: "\n")
                 let recsText = analysis.recommendations.map { "- " + $0 }.joined(separator: "\n")

                 Logger.shared.debug("🎨 Markdown: Insights text length: \(insightsText.count), Recs text length: \(recsText.count)", source: "MarkdownGen")

                 md += "\n## 💡 AI 인사이트\n\n"
                 md += "### 📝 요약\n"
                 md += "\(analysis.summary)\n\n"
                 md += "### 🔍 핵심 인사이트\n"
                 md += "\(insightsText)\n\n"
                 md += "### 💪 실행 가능한 제안\n"
                 md += "\(recsText)\n\n"
                 md += "*Generated by \(analysis.provider) (\(analysis.model))*\n\n"

                 Logger.shared.debug("🎨 Markdown: AI section added to markdown. Current md length: \(md.count)", source: "MarkdownGen")
             } else {
                 Logger.shared.error("🎨 Markdown: Analysis is nil! No AI insights section added", source: "MarkdownGen")
             }
        }

        // Category breakdown - use app names as categories for simplicity
        if blocks.includeCategoriesAndApps {
            md += "\n## 📂 카테고리별 시간 분배\n\n"
            md += "| 카테고리 | 시간 | 비율 |\n"
            md += "|---------|------|------|\n\n"

            for app in stats.topApps.prefix(5) {
                md += "| \(app.appName) | \(String(format: "%.1f", app.duration / 3600))h | \(String(format: "%.1f", app.percentage))% |\n"
            }
            md += "\n"
        }

        // App usage statistics
        if blocks.includeCategoriesAndApps {
            md += "\n## 📱 앱 사용 통계\n\n"
            md += "| 앱/도구 | 사용 시간 |\n"
            md += "|---------|----------|\n\n"

            for app in stats.topApps.prefix(10) {
                md += "| \(app.appName) | \(String(format: "%.1f", app.duration / 3600))h |\n"
            }
            md += "\n"
        }

        // Charts section - only if charts are available
        if blocks.includeCharts && !chartPaths.isEmpty {
            md += "\n\n## 📈 시각화\n\n"

            for chartPath in chartPaths {
                let fileName = chartPath.lastPathComponent
                md += "### \(fileName)\n"
                md += "![[charts/\(fileName)]]\n\n"
            }
        }

        if blocks.includeFooter {
            md += "\n\n---\n\n"
            md += "*📅 Generated by DayArc on \(Date().formatted(date: .numeric, time: .omitted))*\n"
        }

        Logger.shared.debug("🎨 Markdown: Final markdown length: \(md.count) chars", source: "MarkdownGen")

        // Log if AI section is present
        if md.contains("## 💡 AI 인사이트") {
            Logger.shared.info("✅ Markdown: AI insights section IS present in final markdown", source: "MarkdownGen")
        } else {
            Logger.shared.error("❌ Markdown: AI insights section NOT found in final markdown!", source: "MarkdownGen")
        }

        return md
    }

